    /// <summary>Best top-level visible window for a process (MainWindowHandle is often 0 for DX games).</summary>
    public static IntPtr FindBestWindowForProcess(int pid)
    {
        IntPtr best = IntPtr.Zero;
        var bestArea = 0;
        var bestTitled = false;

        EnumWindows((hWnd, _) =>
        {
            GetWindowThreadProcessId(hWnd, out var wpid);
            if (wpid != (uint)pid)
                return true;
            if (!IsWindowVisible(hWnd))
                return true;
            if (GetWindow(hWnd, GW_OWNER) != IntPtr.Zero)
                return true;
            if (!GetClientRect(hWnd, out var rc) || rc.Width <= 0 || rc.Height <= 0)
                return true;

            var title = new StringBuilder(256);
            GetWindowText(hWnd, title, title.Capacity);
            var titled = title.Length > 0;
            var area = rc.Width * rc.Height;

            // Prefer titled windows; otherwise largest client area.
            if (best == IntPtr.Zero
                || (titled && !bestTitled)
                || (titled == bestTitled && area > bestArea))
            {
                best = hWnd;
                bestArea = area;
                bestTitled = titled;
            }
            return true;
        }, IntPtr.Zero);

        return best;
    }

